// 1.服务器端渲染
// 优点
// (1)前端耗时少，对移动端更省电
// (2)有利于SEO, 爬虫更获得数据
// 缺点
//     (1)开发效率低
//     (2)占用服务端资源



// 2.前后端分离
// 后端只负责提供接口，前端只负责调用接口的模式
    // (1)优点
// 开发体验好
//用户体验好
//减轻服务器端的渲染压力
// (2)不利于seo,可以利于vue react技术解决SEO问题


// 3.不谈业务逻辑盲目选择何种开发模式都是耍流氓
// (1)企业级网站没有复杂的交互，这时我们就需要使用服务器渲染
// (2)后台管理项目，交互性较强，用前后端分离



